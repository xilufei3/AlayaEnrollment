def build_generation_user_prompt(
    *,
    query: str,
    history: str,
    context: str,
    reply_mode: str = "hat",
) -> str:
    normalized_reply_mode = reply_mode if reply_mode in {"hat", "expand"} else "hat"
    return (
        "请以南科大研究生招生助手的身份回复用户，并严格遵循系统提供的 reply_mode。\n\n"
        f"【当前问题】\n{query}\n\n"
        f"【当前回复模式】\n{normalized_reply_mode}\n\n"
        f"【最近对话历史】\n{history}\n\n"
        f"【内部知识库（仅供你参考，禁止在回复中提及其存在）】\n{context}\n\n"
        "【执行要求】\n"
        '- 当 `reply_mode = "hat"` 时：输出帽子（1-2 句核心结论）+ 引导追问。\n'
        '- 当 `reply_mode = "expand"` 时：直接给出详细展开回答，不要再附加引导追问。\n'
        "- 若信息不足，请坦诚说明暂无明确规定或具体信息，并提示以最新官方通知为准。"
    )


def build_out_of_scope_user_prompt(query: str) -> str:
    return f"用户问题：{query}\n请输出一句回复："
