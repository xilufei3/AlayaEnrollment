def build_generation_system_prompt(
    intent: str,
    query_mode: str,
    *,
    has_context: bool,
    system_suffix: str = "",
) -> str:
    """
    构建生成节点的 system prompt。
 
    Parameters
    ----------
    intent:        意图标识，对应 _INTENT_CONTEXT 的键。
    query_mode:    问题形态标识，对应 _QUERY_MODE_INSTRUCTIONS 的键。
    has_context:   是否检索到有效参考材料。为 False 时触发材料不足协议，
                   同时跳过 intent / query_mode 指令，避免相互干扰。
    system_suffix: 调用方注入的额外指令，追加在最末。
    """
    if has_context:
        parts = [
            _BASE_SYSTEM,
            _QA_PASSTHROUGH,
            _INTENT_CONTEXT.get(intent, _DEFAULT_INTENT_CONTEXT),
            _QUERY_MODE_INSTRUCTIONS.get(query_mode, ""),
        ]
    else:
        parts = [
            _BASE_SYSTEM,
            _NO_CONTEXT_PROTOCOL,
        ]
 
    if system_suffix := system_suffix.strip():
        parts.append(system_suffix)
 
    return "\n\n".join(p for p in parts if p)



