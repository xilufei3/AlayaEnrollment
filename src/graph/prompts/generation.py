def build_generation_system_prompt(
    intent: str,
    query_mode: str,
    *,
    has_context: bool,
    system_suffix: str = "",
) -> str:
    parts = [
        _SHARED_GENERATION_STYLE,
        _GENERATION_INTENT_PROMPTS.get(intent, _DEFAULT_GENERATION_PROMPT),
        _GENERATION_QUERY_MODE_PROMPTS.get(query_mode, ""),
    ]
    if not has_context:
        parts.append(_NO_RETRIEVAL_GUIDANCE)
    if system_suffix:
        parts.append(system_suffix.strip())
    return "\n\n".join(part for part in parts if part)



def build_generation_user_prompt(
    *, query: str, query_mode: str, history: str, context: str
) -> str:
    return f"""
请完成本轮问答，严格按下面规则处理输入信息：

【信息使用优先级】
1. “用户当前问题”是本轮唯一需要直接回答的问题。
2. “可用材料”是回答事实内容的主要依据。
3. “最近对话”只用于补充理解用户指代、省略和上下文，不作为优先事实依据。

【用户当前问题】
{query}

【问题形态】
{query_mode}

【最近对话】
{history}

【可用材料】
{context}

【回答要求】
1. 先直接回答用户当前这个问题，不要偏题。
2. 不要补充用户没有问到的分析或建议，除非这些信息对当前问题有直接帮助。
3. 当问题属于`introduction`或`factual_query`时，才在同一主题下适度补充相关维度。
4. 禁止使用任何 Markdown 格式（包括 **加粗**、| 表格 |、# 标题、- 列表符号等）。回答只用纯文字、换行、数字序号（1. 2. 3.）组织，像微信消息一样自然易读。
5. 如果使用 SQL 结构化结果，除非用户明确只问某个单一字段，否则尽量完整展示结果条目里已返回的列，不要漏掉字段。
6. 如果使用 SQL 结构化结果输出表格，优先按照字段说明中的列顺序组织表头。
""".strip()
