_DEFAULT_GENERATION_PROMPT = """
这是南科大本科招生咨询场景。
请直接回答用户当前的问题，先说结论，再补充必要说明和相关实用信息，让回答有信息量、一次到位。
""".strip()


_NO_RETRIEVAL_GUIDANCE = """
你没有找到能支撑这个问题的官方材料，无法给出可靠答案。

直接告诉用户你暂时没有这个信息，语气真诚自然，像在认真帮他解决问题，
而不是在播报系统提示。把官方渠道自然地带进回复里，不要单独列清单。

官方渠道：
招生网 https://admission.sustech.edu.cn
招办电话 0755-88010401
""".strip()



def build_generation_system_prompt(
    intent: str,
    *,
    has_context: bool,
    system_suffix: str = "",
) -> str:
    parts = [
        _SHARED_GENERATION_STYLE,
        _GENERATION_INTENT_PROMPTS.get(intent, _DEFAULT_GENERATION_PROMPT),
    ]
    if not has_context:
        parts.append(_NO_RETRIEVAL_GUIDANCE)
    if system_suffix:
        parts.append(system_suffix.strip())
    return "\n\n".join(part for part in parts if part)


def build_generation_user_prompt(*, query: str, history: str, context: str) -> str:
    return (
        f"用户问题：{query}\n"
        f"最近对话：\n{history}\n"
        f"可用材料：\n{context}\n"
        "请回答用户当前这个问题，先根据用户问题确定要覆盖的核心范围，再从可用材料里挑选匹配信息。\n"
        "要求：\n"
        "1. 先回答核心问题，再围绕同一话题补充直接相关的实用信息（注意事项、时间节点、常见误区等），让回答全面、一次到位。\n"
        "2. 当用户的问题限定了年份、省份或其他范围时，优先遵守这些限制，再选择材料；不要因为引用材料而引入与问题不符的年份或主题。\n"
        "3. 充分利用可用材料，不要遗漏材料中与问题相关的重要信息。\n"
        "4. 当材料里存在明确对比维度时，优先用简洁表格；否则用结构化的短列表或分段落呈现。\n"
        "5. 允许基于材料做直接且确定的推理，但不要在推理基础上继续叠加猜测。\n"
        "6. 不要跳到与当前话题无关的其他话题。"
    )
